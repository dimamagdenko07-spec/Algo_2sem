#ifndef FUZZY_H
#define FUZZY_H

#include "../index/index.h"
#include "../index/search.h"
#include "../vector/generic.h"

/*
===============================================================================
Структура: FuzzyCandidate
Назначение: Хранит кандидата для нечёткого поиска

Поля:
- term:      Термин из индекса, похожий на поисковый запрос
- distance:  Расстояние Левенштейна между term и запросом
- postings:  Posting list, связанный с данным термином

===============================================================================
*/
typedef struct {
    char    term[256];
    int     distance;
    Vector* postings;
} FuzzyCandidate;

/*
===============================================================================
Функция: levenshteinDistance
Назначение: Вычисляет расстояние Левенштейна между двумя строками

Аргументы:
- s1: Первая строка
- s2: Вторая строка

Возвращает:
- Минимальное количество операций вставки, удаления или замены,
  необходимых для преобразования одной строки в другую

===============================================================================
*/
int levenshteinDistance(const char* s1, const char* s2);

/*
===============================================================================
Функция: fuzzyFindCandidates
Назначение: Находит все термины индекса, находящиеся на расстоянии
            не больше max_distance от заданного term

Аргументы:
- idx:          Указатель на индекс
- term:         Поисковый термин
- max_distance: Максимально допустимое расстояние Левенштейна

Возвращает:
- Vector с элементами типа FuzzyCandidate
- NULL при ошибке или отсутствии подходящих кандидатов

Поведение:
- Выполняет обход всего индекса
- Для каждого термина вычисляет расстояние Левенштейна
- Если distance <= max_distance, термин добавляется в результат

===============================================================================
*/
Vector* fuzzyFindCandidates(Index* idx, const char* term, int max_distance);

/*
===============================================================================
Функция: fuzzySearch
Назначение: Выполняет нечёткий поиск по запросу с ранжированием результатов

Аргументы:
- idx:          Указатель на индекс
- query:        Поисковый запрос
- max_distance: Максимально допустимое расстояние Левенштейна

Возвращает:
- Указатель на структуру SearchResults
- NULL при ошибке поиска

Поведение:
- Находит похожие термины через fuzzyFindCandidates
- Объединяет posting list найденных терминов
- Формирует SearchResults
- Более близкие термины получают более высокий приоритет

===============================================================================
*/
SearchResults* fuzzySearch(Index* idx, const char* query, int max_distance);

#endif