#pragma once

#include "index.h"

/*
===============================================================================
Структура: SearchResult
Назначение: Хранит один результат поискового запроса

Поля:
- doc_id: Идентификатор найденного документа
- title:  Название найденного документа
- score:  Оценка релевантности документа запросу

===============================================================================
*/
typedef struct {
    int  doc_id;
    char title[MAX_TITLE_LEN];
    int  score;
} SearchResult;

/*
===============================================================================
Структура: SearchResults
Назначение: Хранит общий результат выполнения поискового запроса

Поля:
- results: Vector с элементами SearchResult
- total:   Общее количество найденных документов
- time_ms: Время выполнения поиска в миллисекундах

===============================================================================
*/
typedef struct {
    Vector* results;
    int     total;
    double  time_ms;
} SearchResults;

/*
===============================================================================
Функция: intersectPostings
Назначение: Находит пересечение нескольких posting list

Аргументы:
- lists: Массив указателей на posting list
- n:     Количество списков в массиве lists

Возвращает:
- Новый Vector с документами, которые встречаются во всех списках
- NULL при ошибке или отсутствии пересечения

===============================================================================
*/
Vector* intersectPostings(Vector** lists, int n);

/*
===============================================================================
Функция: search
Назначение: Выполняет поиск документов по текстовому запросу

Аргументы:
- idx:   Указатель на поисковый индекс
- query: Строка поискового запроса

Возвращает:
- Указатель на структуру SearchResults
- NULL при ошибке выполнения поиска

Поведение:
- Разбирает query на отдельные термины
- Ищет posting list для каждого термина
- Формирует итоговый список найденных документов
- Сохраняет количество результатов и время выполнения поиска

===============================================================================
*/
SearchResults* search(Index* idx, const char* query);

/*
===============================================================================
Функция: printResultsText
Назначение: Выводит результаты поиска в текстовом формате

Аргументы:
- sr: Указатель на результаты поиска

===============================================================================
*/
void printResultsText(const SearchResults* sr);

/*
===============================================================================
Функция: printResultsJSON
Назначение: Выводит результаты поиска в JSON-формате

Аргументы:
- sr: Указатель на результаты поиска

===============================================================================
*/
void printResultsJSON(const SearchResults* sr);

/*
===============================================================================
Функция: freeSearchResults
Назначение: Освобождает память, занятую результатами поиска

Аргументы:
- sr: Указатель на структуру SearchResults

Поведение:
- Освобождает Vector с результатами
- Освобождает саму структуру SearchResults

===============================================================================
*/
void freeSearchResults(SearchResults* sr);