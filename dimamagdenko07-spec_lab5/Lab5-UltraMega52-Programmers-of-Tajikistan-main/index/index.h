#pragma once

#include "../posting.h"

typedef enum { TREE_AVL, TREE_RB, TREE_BTREE } TreeType;

/*
===============================================================================
Структура: Index
Назначение: Универсальная оболочка над разными реализациями дерева индекса

Поля:
- tree: Указатель на конкретную структуру дерева
- type: Тип дерева, который лежит внутри index

===============================================================================
*/
typedef struct {
    void*    tree;
    TreeType type;
} Index;

/*
===============================================================================
Функция: createIndex
Назначение: Создаёт пустой поисковый индекс выбранного типа

Аргументы:
- type: Тип дерева, которое будет использоваться внутри индекса

Возвращает:
- Указатель на новую структуру Index
- NULL при ошибке создания индекса или некорректном типе дерева

===============================================================================
*/
Index* createIndex(TreeType type);

/*
===============================================================================
Функция: insertTerm
Назначение: Добавляет один термин в поисковый индекс

Аргументы:
- idx:    Указатель на индекс
- term:   Термин, который нужно добавить
- doc_id: Идентификатор документа, где встретился термин
- title:  Название документа

Поведение:
- Выбирает нужную функцию вставки в зависимости от типа дерева
- Если термин уже существует, обновляет его posting list

===============================================================================
*/
void insertTerm(Index* idx, const char* term, int doc_id, const char* title);

/*
===============================================================================
Функция: lookupTerm
Назначение: Ищет posting list по заданному термину

Аргументы:
- idx:  Указатель на индекс
- term: Термин для поиска

Возвращает:
- Указатель на Vector с элементами PostingEntry
- NULL, если термин не найден

===============================================================================
*/
Vector* lookupTerm(const Index* idx, const char* term);

/*
===============================================================================
Функция: indexDocument
Назначение: Добавляет в индекс все токены одного документа

Аргументы:
- idx:      Указатель на индекс
- doc_id:   Идентификатор документа
- title:    Название документа
- tokens:   Массив строковых токенов документа
- n_tokens: Количество токенов в массиве tokens

===============================================================================
*/
void indexDocument(Index* idx, int doc_id, const char* title,
                   const char** tokens, int n_tokens);

/*
===============================================================================
Функция: traverseIndex
Назначение: Обходит весь индекс и применяет функцию visit к каждому термину

Аргументы:
- idx:   Указатель на индекс
- visit: Функция, вызываемая для каждого термина
- ctx:   Дополнительный пользовательский контекст

Аргументы функции visit:
- key:      Текущий термин
- postings: Posting list текущего термина
- ctx:      Пользовательский контекст, переданный в traverseIndex

===============================================================================
*/
void traverseIndex(
    const Index* idx,
    void (*visit)(const char* key, Vector* postings, void* ctx),
    void* ctx
);

/*
===============================================================================
Функция: saveIndex
Назначение: Сохраняет поисковый индекс в файл

Аргументы:
- idx:  Указатель на индекс
- path: Путь к файлу, куда нужно сохранить индекс

===============================================================================
*/
void saveIndex(const Index* idx, const char* path);

/*
===============================================================================
Функция: loadIndex
Назначение: Загружает поисковый индекс из файла

Аргументы:
- path: Путь к файлу, из которого нужно загрузить индекс
- type: Тип дерева, которое нужно использовать для загруженного индекса

Возвращает:
- Указатель на загруженный Index
- NULL при ошибке загрузки или создания индекса

===============================================================================
*/
Index* loadIndex(const char* path, TreeType type);

/*
===============================================================================
Функция: freeIndex
Назначение: Полностью очищает индекс и освобождает всю выделенную память

Аргументы:
- idx: Указатель на индекс

Поведение:
- Освобождает внутреннее дерево в зависимости от type
- Освобождает саму структуру Index

===============================================================================
*/
void freeIndex(Index* idx);