#pragma once

#include "vector/generic.h"

#define MAX_TITLE_LEN 256

/*
===============================================================================
Структура: PostingEntry
Назначение: Хранит информацию об одном документе, в котором встретился термин

Поля:
- doc_id: Уникальный идентификатор документа
- title:  Название документа

===============================================================================
*/
typedef struct {
    int  doc_id;
    char title[MAX_TITLE_LEN];
} PostingEntry;

/*
===============================================================================
Тип данных: Posting list
Назначение: Список документов, связанных с одним термином

Описание:
- Posting list реализован как Vector
- Каждый элемент Vector имеет тип PostingEntry
- Для создания списка используется createPostingList
- Для добавления документа используется appendPosting
- Для копирования списка используется clonePostingList
- Освобождать список нужно через vectorFree

===============================================================================
*/


/*
===============================================================================
Функция: createPostingList
Назначение: Создаёт пустой posting list для хранения элементов PostingEntry

Возвращает:
- Указатель на Vector, хранящий элементы типа PostingEntry
- NULL при ошибке создания вектора

===============================================================================
*/
Vector* createPostingList(void);

/*
===============================================================================
Функция: appendPosting
Назначение: Добавляет информацию о документе в posting list

Аргументы:
- list:   Указатель на posting list
- doc_id: Идентификатор документа
- title:  Название документа

===============================================================================
*/
void appendPosting(Vector* list, int doc_id, const char* title);

/*
===============================================================================
Функция: clonePostingList
Назначение: Создаёт копию существующего posting list

Аргументы:
- list: Указатель на исходный posting list

Возвращает:
- Указатель на новый Vector с копиями элементов исходного списка
- NULL при ошибке копирования или некорректных данных

===============================================================================
*/
Vector* clonePostingList(const Vector* list);