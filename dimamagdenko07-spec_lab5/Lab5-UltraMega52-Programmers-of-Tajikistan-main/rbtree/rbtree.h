#pragma once

#include "../posting.h"
#include "../vector/generic.h"

typedef enum { RB_RED, RB_BLACK } RBColor;

/*
===============================================================================
Структура: RBNode
Назначение: Узел красно-чёрного дерева, хранящий термин и список документов

Поля:
- key:      Строковый ключ, то есть индексируемый термин
- color:    Цвет узла
- postings: Posting list со всеми документами, где встретился key
- left:     Указатель на левое поддерево
- right:    Указатель на правое поддерево
- parent:   Указатель на родительский узел

===============================================================================
*/
typedef struct RBNode {
    char*           key;
    RBColor         color;
    Vector*         postings;
    struct RBNode*  left;
    struct RBNode*  right;
    struct RBNode*  parent;
} RBNode;

/*
===============================================================================
Структура: RBTree
Назначение: Красно-чёрное дерево для хранения терминов поискового индекса

Поля:
- root: Указатель на корень дерева
- nil:  Sentinel-узел, используемый как общий чёрный лист
- size: Количество различных ключей в дереве

===============================================================================
*/
typedef struct {
    RBNode* root;
    RBNode* nil;
    int     size;
} RBTree;

/*
===============================================================================
Функция: createRBTree
Назначение: Создаёт пустое красно-чёрное дерево

Возвращает:
- Указатель на новую структуру RBTree
- NULL при ошибке выделения памяти

===============================================================================
*/
RBTree* createRBTree(void);

/*
===============================================================================
Функция: freeRBTree
Назначение: Полностью очищает красно-чёрное дерево и освобождает память

Аргументы:
- tree: Указатель на красно-чёрное дерево

===============================================================================
*/
void freeRBTree(RBTree* tree);

/*
===============================================================================
Функция: rbInsert
Назначение: Добавляет термин в красно-чёрное дерево или обновляет его posting list

Аргументы:
- tree:   Указатель на красно-чёрное дерево
- key:    Термин, который нужно добавить
- doc_id: Идентификатор документа, где встретился термин
- title:  Название документа

Поведение:
- Если key уже есть в дереве, новый документ добавляется в postings
- Если key отсутствует, создаётся новый узел
- После вставки дерево сохраняет свойства красно-чёрного дерева

===============================================================================
*/
void rbInsert(RBTree* tree, const char* key, int doc_id, const char* title);

/*
===============================================================================
Функция: rbSearch
Назначение: Ищет posting list по заданному термину

Аргументы:
- tree: Указатель на красно-чёрное дерево
- key:  Термин для поиска

Возвращает:
- Указатель на Vector с элементами PostingEntry
- NULL, если термин не найден

===============================================================================
*/
Vector* rbSearch(const RBTree* tree, const char* key);

/*
===============================================================================
Функция: rbTraverse
Назначение: Обходит красно-чёрное дерево и применяет функцию visit к каждому ключу

Аргументы:
- tree:  Указатель на красно-чёрное дерево
- visit: Функция, вызываемая для каждого элемента дерева
- ctx:   Дополнительный пользовательский контекст

Аргументы функции visit:
- key:      Текущий термин
- postings: Posting list текущего термина
- ctx:      Пользовательский контекст, переданный в rbTraverse

===============================================================================
*/
void rbTraverse(
    const RBTree* tree,
    void (*visit)(const char* key, Vector* postings, void* ctx),
    void* ctx
);