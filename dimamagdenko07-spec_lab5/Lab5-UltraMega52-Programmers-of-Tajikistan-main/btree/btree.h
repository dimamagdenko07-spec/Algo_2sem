#pragma once

#include "../posting.h"
#include "../vector/generic.h"

#define BTREE_T        3
#define BTREE_MAX_KEYS (2 * BTREE_T - 1)
#define BTREE_MAX_CH   (2 * BTREE_T)

/*
===============================================================================
Константы B-дерева
Назначение: Определяют минимальную степень дерева и размеры массивов в узле

Значения:
- BTREE_T:        Минимальная степень B-дерева
- BTREE_MAX_KEYS: Максимальное количество ключей в одном узле
- BTREE_MAX_CH:   Максимальное количество дочерних узлов

===============================================================================
*/

/*
===============================================================================
Структура: BTreeNode
Назначение: Узел B-дерева, хранящий несколько ключей и несколько потомков

Поля:
- keys:     Массив строковых ключей
- postings: Массив posting list, соответствующих ключам из keys
- children: Массив указателей на дочерние узлы
- n:        Текущее количество ключей в узле
- is_leaf:  Флаг, показывающий, является ли узел листом

===============================================================================
*/
typedef struct BTreeNode {
    char*             keys[BTREE_MAX_KEYS];
    Vector*           postings[BTREE_MAX_KEYS];
    struct BTreeNode* children[BTREE_MAX_CH];
    int               n;
    int               is_leaf;
} BTreeNode;

/*
===============================================================================
Структура: BTree
Назначение: B-дерево для хранения терминов поискового индекса

Поля:
- root: Указатель на корень дерева
- size: Количество различных ключей в дереве

===============================================================================
*/
typedef struct {
    BTreeNode* root;
    int        size;
} BTree;

/*
===============================================================================
Функция: createBTree
Назначение: Создаёт пустое B-дерево

Возвращает:
- Указатель на новую структуру BTree
- NULL при ошибке выделения памяти

===============================================================================
*/
BTree* createBTree(void);

/*
===============================================================================
Функция: freeBTree
Назначение: Полностью очищает B-дерево и освобождает всю выделенную память

Аргументы:
- tree: Указатель на B-дерево

===============================================================================
*/
void freeBTree(BTree* tree);

/*
===============================================================================
Функция: btreeInsert
Назначение: Добавляет термин в B-дерево или обновляет его posting list

Аргументы:
- tree:   Указатель на B-дерево
- key:    Термин, который нужно добавить
- doc_id: Идентификатор документа, где встретился термин
- title:  Название документа

===============================================================================
*/
void btreeInsert(BTree* tree, const char* key, int doc_id, const char* title);

/*
===============================================================================
Функция: btreeSearch
Назначение: Ищет posting list по заданному термину

Аргументы:
- tree: Указатель на B-дерево
- key:  Термин для поиска

Возвращает:
- Указатель на Vector с элементами PostingEntry
- NULL, если термин не найден

===============================================================================
*/
Vector* btreeSearch(const BTree* tree, const char* key);

/*
===============================================================================
Функция: btreeTraverse
Назначение: Обходит B-дерево и применяет функцию visit к каждому ключу

Аргументы:
- tree:  Указатель на B-дерево
- visit: Функция, вызываемая для каждого ключа
- ctx:   Дополнительный пользовательский контекст

Аргументы функции visit:
- key:      Текущий термин
- postings: Posting list текущего термина
- ctx:      Пользовательский контекст, переданный в btreeTraverse

===============================================================================
*/
void btreeTraverse(
    const BTree* tree,
    void (*visit)(const char* key, Vector* postings, void* ctx),
    void* ctx
);