#pragma once

#include "../posting.h"

/*
===============================================================================
Структура: AVLNode
Назначение: Узел AVL-дерева, хранящий термин и список документов с этим термином

Поля:
- key:      Строковый ключ, то есть индексируемый термин
- height:   Высота узла, используемая для балансировки AVL-дерева
- postings: Posting list со всеми документами, где встретился key
- left:     Указатель на левое поддерево
- right:    Указатель на правое поддерево

===============================================================================
*/
typedef struct AVLNode {
    char*           key;
    int             height;
    Vector*         postings;
    struct AVLNode* left;
    struct AVLNode* right;
} AVLNode;

/*
===============================================================================
Структура: AVLTree
Назначение: AVL-дерево для хранения терминов поискового индекса

Поля:
- root: Указатель на корень дерева
- size: Количество различных ключей в дереве

===============================================================================
*/
typedef struct {
    AVLNode* root;
    int      size;
} AVLTree;

/*
===============================================================================
Функция: createAVLTree
Назначение: Создаёт пустое AVL-дерево

Возвращает:
- Указатель на новую структуру AVLTree
- NULL при ошибке выделения памяти

===============================================================================
*/
AVLTree* createAVLTree(void);

/*
===============================================================================
Функция: freeAVLTree
Назначение: Полностью очищает AVL-дерево и освобождает всю выделенную память

Аргументы:
- tree: Указатель на AVL-дерево

===============================================================================
*/
void freeAVLTree(AVLTree* tree);

/*
===============================================================================
Функция: avlInsert
Назначение: Добавляет термин в AVL-дерево или обновляет его posting list

Аргументы:
- tree:   Указатель на AVL-дерево
- key:    Термин, который нужно добавить
- doc_id: Идентификатор документа, где встретился термин
- title:  Название документа

Поведение:
- Если key уже есть в дереве, новый документ добавляется в postings
- Если key отсутствует, создаётся новый узел
- После вставки дерево сохраняет AVL-балансировку

===============================================================================
*/
void avlInsert(AVLTree* tree, const char* key, int doc_id, const char* title);

/*
===============================================================================
Функция: avlSearch
Назначение: Ищет posting list по заданному термину

Аргументы:
- tree: Указатель на AVL-дерево
- key:  Термин для поиска

Возвращает:
- Указатель на Vector с элементами PostingEntry
- NULL, если термин не найден

===============================================================================
*/
Vector* avlSearch(const AVLTree* tree, const char* key);

/*
===============================================================================
Функция: avlTraverse
Назначение: Обходит AVL-дерево и применяет функцию visit к каждому ключу

Аргументы:
- tree:  Указатель на AVL-дерево
- visit: Функция, вызываемая для каждого элемента дерева
- ctx:   Дополнительный пользовательский контекст

Аргументы функции visit:
- key:      Текущий термин
- postings: Posting list текущего термина
- ctx:      Пользовательский контекст, переданный в avlTraverse

===============================================================================
*/
void avlTraverse(
    const AVLTree* tree,
    void (*visit)(const char* key, Vector* postings, void* ctx),
    void* ctx
);